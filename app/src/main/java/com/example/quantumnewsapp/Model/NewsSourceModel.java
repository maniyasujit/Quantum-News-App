package com.example.quantumnewsapp.Model;

public class NewsSourceModel {
    String id, name;

    public NewsSourceModel() {
    }

    public NewsSourceModel(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
